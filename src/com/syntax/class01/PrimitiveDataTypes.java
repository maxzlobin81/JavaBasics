package src.com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		// storing and representing whole numeric values
		
    byte box1 = 127;
    short box2 = 32767;
    int box3 = 100000; // popular
    long box4 = 100000000l;
    
    
    // storing and representing decimal numbers
    
    float variable1=1.99f;
    double variable2=99.99;
    
    // storing single characters 
    
    char container='A';
    char dollar='$'; 
    
    // storing yes or no values
    
    boolean hot=true; 
    boolean tired=false;
    
    System.out.println(box1);
    System.out.println(box2);
    System.out.println(box3);
    System.out.println(box4);
    
    System.out.println(variable1);// printing value of the variable	
    System.out.println(variable2);// printing text
    
    System.out.println(container);
    System.out.println(dollar);
	
    System.out.println(hot);
    System.out.println(tired);
			
	}

}
